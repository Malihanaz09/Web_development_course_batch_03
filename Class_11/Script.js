// Comparison operator
let a = 10;
let b = 15;
console.log(a==b); //10=15 false
console.log(a!=b); //10/=15 true

console.log(a!==b); //10=15 false
console.log(a===b); //10=15 false
console.log(a>b); 
console.log(a<b);

// Logical operator
let cond1 = a==b; //false
let cond2 = a!=b; //true
console.log("logical operators");   
console.log(cond1 && cond2);        
console.log(cond1 || cond2);   

